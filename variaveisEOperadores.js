// const também é uma váriavel, porém é utilizada para declarar uma váriavel cujo valor é fixo, ou seja, uma vez atribuído um valor a uma constante, esse valor não pode ser mais alterado posteriormente.
const pi = 3.14;

console.log(variavel) //invocando uma função para mostrar no console
console.log(pi)

